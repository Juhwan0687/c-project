#include <stdio.h>
#include <stdlib.h>

int main()
{
	printf("%d 학년 %d반\n", 5, 6);
	printf("%.1fcm %.1lfkg\n", 142.3, 37.2);
	printf("나의 이름은 %s이고 혈액형은 %c형입니다.\n", "김주환", 'O');
	printf("나는 %d살입니다.\n", 12);
	printf("나의 시력은 %.1f/%.1f입니다.", 1.5, 1.5);
	return 0;
}

